# 5-6 변수와 자료형 - 숫자(1)

# 정수에 대해서

# a에 2를 선언, b에 3을 선언
a = 2
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)

# 이 외에도 파이썬에서는 더 많은 연산이 있음

# 제곱수를 구하는 식. 밑에 식은 a의 b승의 계산 결과를 알려줌
print(a ** b)

# 더하기 연산을 먼저한다고 명령. 괄호를 하지않는다면 곱하기를 우선으로 처리함
print((a + b) * b)

# 나누기의 결과로 몫을 얻는 방법
print(a // b)

# 나누기의 결과로 나머지를 얻는 방법
print(a % b)

# 실수에 대해서 - 십진 부동 소수점

x = 10.0
y = 0.3
z = 1

print(x + y)
print(x - y)
print(x * y)
print(x / y)

print(x + z)
print(x - z)
print(x * z)
print(x / z)

# 실수와 연산하는 수는 그 결과가 모두 실수이다