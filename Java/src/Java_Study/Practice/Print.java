package Java_Study.Practice;

//Java 기본 세팅 관련 단축키
public class Print {
    //psvm 입력 시 자동 생성
    public static void main(String[] arg){
        //souf 입력 시 자동 생성
        System.out.printf("souf : 서식이 지정된 문자열을 System.out으로 출력");
        //sout 입력 시 자동 생성
        System.out.println("sout : 문자열을 System.out으로 출력");
        //soutm 입력 시 자동 생성
        System.out.println("soutm : 현재 클래스 및 메서드 이름을 System.out으로 출력");
        //soutp 입력 시 자동 생성
        System.out.println("soutp : 메서드 매개변수 이름 및 값을 System.out으로 출력");
        //soutv 입력 시 자동 생성
        System.out.println("soutv : 값을 System.out으로 출력");
    }
}
